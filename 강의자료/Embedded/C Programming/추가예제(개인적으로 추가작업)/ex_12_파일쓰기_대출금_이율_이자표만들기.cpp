/*	11. 파일 입출력
	for문을 이용 대출금, 대출이율, 대출이자를 표로 만들어 파일로 저장하기
*/

#include <stdio.h>		

int main()
{
	int loan, days = 30, i;			// 대출금, 대출한 일 수	
	double rate, interest;			// 연 이율, 대출금의 월 이자
	FILE * fp;		

	fp = fopen("rate_table.txt", "w");	// 쓰기용 파일 열기

	fprintf(fp, "\n    >> 대출금 대출이율별 대출이자 표 << \n\n");
	fprintf(fp, "대출금 | ");
	for (loan = 1000000; loan < 5000000; loan += 1000000)
		fprintf(fp, "%9d ", loan);
	fprintf(fp, "\n---------------------------------------------------");

	for (rate = 3; rate <= 15; rate+=.5)
	{
		fprintf(fp, "\n %4.1lf%% |", rate);
		for (loan = 1000000; loan < 5000000; loan += 1000000)
		{
			interest = loan * (rate/100/365) * days;
			fprintf(fp, "%10.0lf", interest);
		}
	}
			
	return 0;
}

