#ifndef __DATA_TYPE_H_
#define __DATA_TYPE_H_


typedef		signed short	int16; // 2바이트 크기의 자료형, 부호있는 데이터 처리
typedef		signed long	long32; // 4바이트 크기의 자료형, 부호있는 데이터 처리
typedef		unsigned char	uchar8; // 1바이트 크기의 자료형, 부호없는 데이터 처리
typedef		unsigned short	uint16; // 2바이트 크기의 자료형, 부호없는 데이터 처리
typedef		unsigned long	ulong32; // 4바이트 크기의 자료형, 부호없는 데이터 처리

#define		uint8_t	    uchar8
#define		uint16_t	    uint16
#define		uint32_t	    ulong32


#endif
